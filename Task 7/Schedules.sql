CREATE TABLE Schedules (
    Schedule_Id INT PRIMARY KEY,
    Course_Id INT,
    Classroom_Id INT,
    Day_of_Week VARCHAR(10),
    Start_Time TIME,
    End_Time TIME,
    FOREIGN KEY (Course_Id) REFERENCES Courses(Course_Id),
    FOREIGN KEY (Classroom_Id) REFERENCES Classrooms(Classroom_Id)
);

