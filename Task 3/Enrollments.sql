CREATE TABLE Enrollments (
    Enrollment_Id INT PRIMARY KEY,
    Student_Id BIGINT,
    Course_Id INT,
    Enrollment_Date DATE,
    Grade CHAR(2),
    FOREIGN KEY (Student_Id) REFERENCES Student_Info(Student_Id),
    FOREIGN KEY (Course_Id) REFERENCES Courses(Course_Id)
);

